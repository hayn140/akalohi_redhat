# Arithmetic Operations
print(10 + 3)
print(10 - 3)
print(10 / 3) # Will return a float
print(10 // 3) # Will return a rounded integer
print(10 % 3) # Modulus: Will return the remainder of the division
print(10 * 3)
print(10 ** 3) # Exponents: Ten to the power of three

# Augmented Assignment Operator
# The long way of incrementing
x = 10
x = x + 3

print(x)

# Accomplishing the same thing using short form Augmented Assignment Operators
y = 13
y += 3

print(y)

y -= 3
print(y)

y *= 3
print(y)

y /= 3
print(y)

